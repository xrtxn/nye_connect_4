package hu.nye;

public enum MenuAction {
    /**
     * The player wants to go back to the main menu.
     */
    MAIN_MENU,

    /**
     * The player wants to start a new game.
     */
    NEW_GAME,

    /**
     * The player wants to load a game.
     */
    LOAD_GAME,

    /**
     * The player wants to save the game.
     */
    SAVE_GAME,

    /**
     * The player wants to see the highscores.
     */
    SHOW_HIGHSCORES,

    /**
     * The player wants to quit the game.
     */
    QUIT
}
